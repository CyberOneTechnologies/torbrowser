import os
import subprocess
import requests

TOR_BROWSER_VERSION = '10.0.18'
TOR_URL = f'https://www.torproject.org/dist/torbrowser/{TOR_BROWSER_VERSION}/tor-browser-linux64-{TOR_BROWSER_VERSION}_en-US.tar.xz'

def download_tor():
    response = requests.get(TOR_URL)
    with open('tor-browser-linux64.tar.xz', 'wb') as file:
        file.write(response.content)

def extract_tor():
    subprocess.run(['tar', '-xf', 'tor-browser-linux64.tar.xz'])

def main():
    print("Downloading Tor Browser...")
    download_tor()
    print("Extracting Tor Browser...")
    extract_tor()
    print("Tor Browser setup is complete!")

if __name__ == "__main__":
    main()
